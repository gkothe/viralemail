package com.taverna.models;

public class Usuario{
	
	private int cod_cidade;
	private String desc_telefone;
	private String num_enderec;
	private String desc_login;
	private String desc_senha;
	private String flag_ativo;
	private String flag_ativado;
	private String chave_ativacao;
	private String desc_novoemailvalidacao;
	private String chave_ativacao_novoemail;
	public int getCod_cidade() {
		return cod_cidade;
	}
	public void setCod_cidade(int cod_cidade) {
		this.cod_cidade = cod_cidade;
	}
	public String getDesc_telefone() {
		return desc_telefone;
	}
	public void setDesc_telefone(String desc_telefone) {
		this.desc_telefone = desc_telefone;
	}
	public String getNum_enderec() {
		return num_enderec;
	}
	public void setNum_enderec(String num_enderec) {
		this.num_enderec = num_enderec;
	}
	public String getDesc_login() {
		return desc_login;
	}
	public void setDesc_login(String desc_login) {
		this.desc_login = desc_login;
	}
	public String getDesc_senha() {
		return desc_senha;
	}
	public void setDesc_senha(String desc_senha) {
		this.desc_senha = desc_senha;
	}
	public String getFlag_ativo() {
		return flag_ativo;
	}
	public void setFlag_ativo(String flag_ativo) {
		this.flag_ativo = flag_ativo;
	}
	public String getFlag_ativado() {
		return flag_ativado;
	}
	public void setFlag_ativado(String flag_ativado) {
		this.flag_ativado = flag_ativado;
	}
	public String getChave_ativacao() {
		return chave_ativacao;
	}
	public void setChave_ativacao(String chave_ativacao) {
		this.chave_ativacao = chave_ativacao;
	}
	public String getDesc_novoemailvalidacao() {
		return desc_novoemailvalidacao;
	}
	public void setDesc_novoemailvalidacao(String desc_novoemailvalidacao) {
		this.desc_novoemailvalidacao = desc_novoemailvalidacao;
	}
	public String getChave_ativacao_novoemail() {
		return chave_ativacao_novoemail;
	}
	public void setChave_ativacao_novoemail(String chave_ativacao_novoemail) {
		this.chave_ativacao_novoemail = chave_ativacao_novoemail;
	}

	
}